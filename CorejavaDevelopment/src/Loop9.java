
public class Loop9 {

	public static void main(String[] args) {
		int i,j,n=5;
		for( i=1;i<=n;i++)
		{
			for(j=1;j<=n;j++) 
			{
				if(i==1|| j==1|| i==5||j==5 )
				{
					System.out.print(" 7 ");				
				}
		
			else 
			{
				System.out.print(" 1 ");
			}
		   }
		System.out.println();
		}
		System.out.println();

	}

}
