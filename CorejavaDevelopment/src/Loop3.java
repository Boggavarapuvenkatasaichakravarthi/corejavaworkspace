import java.util.Scanner;

public class Loop3 {

	public static void main(String[] args) {
       int n;
       Scanner sc=new Scanner(System.in);
       System.out.println("Enter N value:");
       n=sc.nextInt();
       for(int i=1;i<=10;i++)
       {
           System.out.println(i*n);
  
       }
       
	}

}
